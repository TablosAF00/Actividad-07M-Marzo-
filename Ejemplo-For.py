#El buclee For a diferencia del while es que esta definido por un numero de iteraciones desde el comienzo
#Por ejemplo en este bucle for la variable "i" se seguira imprimiendo hasta que se cumpla la condicion inicial
for i in range(0, 5): #A su vez "i" seguira eincrementando su valor en 1 por cada ciclo
    print(i)

# Salida:
# 0
# 1
# 2
# 3
# 4
#Como resultado obtenemos que "i" se imprimio 5 veces
